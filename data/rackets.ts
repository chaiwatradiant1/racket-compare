// Racket data — single source of truth.
// Adding a racket = append ONE entry to RACKET_DATA below. The catalog number
// (`no`) is derived from array order, so you never hand-maintain it.

export type Balance = "Head-Heavy" | "Even" | "Head-Light";
export type Flex = "Extra Stiff" | "Stiff" | "Medium" | "Flexible";
export type PlayStyle = "Power" | "Control" | "All-Around" | "Speed";
export type PriceRange = "$" | "$$" | "$$$";

/** Keys of the five play attributes scored 1–5 (used by the radar chart). */
export type RadarKey =
  | "power"
  | "speed"
  | "maneuverability"
  | "defense"
  | "control";

export interface RadarAxis {
  key: RadarKey;
  label: string;
}

/** A racket as consumed by the UI — includes the derived catalog number. */
export interface Racket {
  /** Slug, must be unique. */
  id: string;
  brand: string;
  model: string;
  /** Release year, kept as a string for display. */
  year: string;
  /** Placeholder URL — empty means use the built-in striped slot. */
  imageUrl: string;
  /** e.g. "4U (83g)". */
  weight: string;
  /** Midpoint grams, for sorting/normalizing. */
  weightGrams: number;
  balance: Balance;
  /** Balance point in mm. */
  balanceMm: number;
  flex: Flex;
  /** 1..5 (5 = stiffest). */
  flexScore: number;
  frameMaterial: string;
  shaftMaterial: string;
  /** e.g. "21-29 lbs". */
  stringTensionMax: string;
  /** Upper bound, for sort/compare. */
  tensionMaxLbs: number;
  gripSize: string;
  stringPattern: string;
  playStyle: PlayStyle;
  priceRange: PriceRange;
  power: number;
  control: number;
  speed: number;
  maneuverability: number;
  defense: number;
  /** Catalog number, e.g. "001". Derived from array order — do not set by hand. */
  no: string;
}

